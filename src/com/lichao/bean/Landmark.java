package com.lichao.bean;

/**
 * 4个关键点位置，左眼中心、右眼中心、鼻尖、嘴中心
 */
public class Landmark {

    private int x;
    private int y;
    public void setX(int x) {
         this.x = x;
     }
     public int getX() {
         return x;
     }

    public void setY(int y) {
         this.y = y;
     }
     public int getY() {
         return y;
     }

}